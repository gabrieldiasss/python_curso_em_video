# Ordem de precedência

# 1° => ()
# 2° => **
# 3° => * / // %
# 4° => + e -

# Exemplos de ordem
print(5 + 3 * 2)
print(3 * 5 + 4 ** 2)
print(3 * (5 + 4) ** 2)

print(19/2)
print(19//2)

# Potencia (perde a ordem de precedencia)
print(pow(4, 3))

# Obter a raiz quadrada
print(81**(1/2))
print(25**(1/2))

# Obter a raiz cubica
print(127**(1/3))

# Concatenando
print('Oi' + 'Olá')

# Imprimir Oi 5x
print('Oi'*5)

