# Tipos primitivos e saída de dados

# Convertendo valor do input em número
n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
sum = n1 + n2

print('A soma vale {} e {} vale {}'.format(n1, n2, sum))

# Int => números inteiros // 9
# Float => Número de ponto flutuante // 4.5
# Bool => Booleano // True e False
# Str => caracteres // 'Olá'

