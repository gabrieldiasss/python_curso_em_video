# ESTRUTURA DE REPETIÇÃO FOR

# Exibir o OI 6 vezes
for c in range(0, 6):
    print('oi')
print('FIM')

# Exibir o número de trás para frente
for c in range(6, 0, -1):
    print(c)
print('FIM')

# Vai exibir de 0 a 6
for c in range(0, 7):
    print(c)
print('FIM')

# Vai exibir de 0 a 6 de 2 em 2
for c in range(0, 7, 2):
    print(c)
print('FIM')

